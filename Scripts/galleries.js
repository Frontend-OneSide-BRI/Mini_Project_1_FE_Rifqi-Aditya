const getDataByCategory = (category) => {
  return new Promise((resolve, reject) => {
    resolve(imageData.find((data) => data.category === category));
  });
};

const renderAllImages = () => {
  const gallery = document.getElementById("gallery");
  gallery.innerHTML = "";

  imageData.map((data) =>
    data.images.map(
      (image) =>
        (gallery.innerHTML += `
            <div class="gallery-item-container col-sm-3">
                <img
                    src=${image.url}
                />
            </div>
        `)
    )
  );
};

const renderImagesByCategory = async (category) => {
  const gallery = document.getElementById("gallery");
  gallery.innerHTML = "";

  await getDataByCategory(category)
    .then((data) =>
      data.images.map(
        (image) =>
          (gallery.innerHTML += `
            <div class="gallery-item-container col-sm-3">
                <img
                    src=${image.url}
                />
            </div>
        `)
      )
    )
    .catch((err) => console.log(err));
};
